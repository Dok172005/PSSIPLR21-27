<?php
// Заданный диаметр окружности
$diameter = 50; // Пример значения диаметра, можно изменить на нужное

// Математическая константа Pi
$pi = M_PI; // M_PI - это предопределенная константа в PHP, содержащая значение числа Pi

// Вычисление длины окружности
$length = $pi * $diameter;

// Вывод результата
echo "Длина окружности с диаметром $diameter равна $length";
?>
