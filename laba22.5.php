<?php
// 1. Создание строковых переменных s1 и s2
$s1 = "Я люблю Беларусь";
$s2 = "Я учусь в Политехническом колледже.";

// 2. Определение длины строки s2
$length_s2 = strlen($s2);
echo "Длина строки s2: $length_s2<br>";

// 3. Выделение 11-го символа в строке s1 и определение ASCII-кода
$n = 11;
$char_at_n = mb_substr($s1, $n - 1, 1); // Получаем символ, учитывая, что нумерация начинается с 0
$ascii_code = ord($char_at_n); // Получаем ASCII-код символа
echo "11-й символ в строке s1: $char_at_n (ASCII-код: $ascii_code)<br>";

// 4. Замена слова "Беларусь" на "Гродно" в строке s1
$s1_modified = str_replace("Беларусь", "Гродно", $s1);
echo "Измененная строка s1: $s1_modified";
?>